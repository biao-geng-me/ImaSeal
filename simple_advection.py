# simple advection
import numpy as np
from scipy.ndimage import map_coordinates
import matplotlib.pyplot as plt

def advect_2d(x, y, u, v, dt):
    """
    Performs Semi-Lagrangian advection on a 2D velocity field.
    
    Parameters:
    x, y  : 2D numpy arrays of the grid coordinates (from np.meshgrid)
    u, v  : 2D numpy arrays of the velocity field components
    dt    : Time step for advection
    
    Returns:
    u_new, v_new : The advected velocity components
    """
    # 1. The Backward Trace
    # Find where the fluid at the current grid points came from dt seconds ago
    x_back = x - u * dt
    y_back = y - v * dt
    
    # 2. Convert physical coordinates to fractional grid indices
    # map_coordinates works on array indices (0, 1, 2.5, etc.), not physical coordinates.
    # We assume a uniform grid generated by np.meshgrid(..., indexing='xy')
    Ny, Nx = x.shape
    
    x_min, x_max = np.min(x), np.max(x)
    y_min, y_max = np.min(y), np.max(y)
    
    dx = (x_max - x_min) / (Nx - 1)
    dy = (y_max - y_min) / (Ny - 1)
    
    # Calculate the fractional index of the source locations
    idx_x = (x_back - x_min) / dx
    idx_y = (y_back - y_min) / dy
    
    # Stack indices. Since numpy arrays are indexed (row, column) -> (y, x)
    indices = np.stack([idx_y, idx_x])
    
    # 3. Interpolate the new velocities
    # order=1 means bilinear interpolation. 
    # mode='nearest' means if we trace a path outside the grid boundaries, 
    # we just grab the velocity of the closest boundary edge.
    u_new = map_coordinates(u, indices, order=1, mode='nearest')
    v_new = map_coordinates(v, indices, order=1, mode='nearest')
    
    return u_new, v_new


